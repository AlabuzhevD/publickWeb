<h1>Принцип действия</h1>
    <p>Радар излучает радиосигнал и регистрирует отраженный сигнал. По изменению частоты радар вычисляет разность скоростей между самим 
    радаром и объектом и таким образом определяет скорость автомобиля.Некоторые типы радаров позволяют проводить измерение скорости
    с движущегося патрульного автомобиля. При этом информацию о собственной скорости патрульного автомобиля радар получает из отраженного от дороги сигнала</p>
    


