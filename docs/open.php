   <h2>Режим работы радара</h2>
   <p >Основополагающие технологии работы радаров: — OEM, Ultra-X, Ultra-K (K-Pulse)/(Smartscan™), Instant-On, POP™, HYPER-X™, HYPER-K™.</p>
     <p >Радары могут комбинировать эти технологии для достижения целей сокрытия сигнала от радар-детектора. Так например «ИСКРА 1» использует одновременно Instant-ON как режим включения и комбинацию PULSE+POP в виде пачки из 5 коротких импульсов</p>
     <p >Instant-ON (Мгновенное включение) — это режим включения радара, когда изначально радар включается и находится в дежурном режиме, но не излучает никакого сигнала. После нажания на кнопку радара он мгновенно начинает излучать сигнал и измеряет скорость цели, на которую он направлен. Это позволяет оставаться незаметным для радар-детекторов, что значительно увеличивает эффективность работы радара, а также экономит энергию батарей радара.</p>
     <p >POP — зарегистрированная торговая марка, принадлежащая MPH Technologies. Эта технология в отличие от Instant-ON отвечает за структуру самого сигнала. Суть технологии заключается в том, что радар после включения излучает очень короткий импульс и с его помощью измеряет скорость цели. Использование этой технологии усложняет детектирование сигнала радара радар-детекторами так как многие модели воспринимают такой импульс как помеху и не выдают никакого предупреждения водителю. Также из-за слишком короткого импульса значительно уменьшается дистанция детектирования. Чтобы радар-детектор мог распознавать POP сигналы радара он должен быть оснащен соответствующей технологией защиты.</p>
     <p >PULSE — кроме POP существует ещё и технология импульсного сигнала. Она отличается от POP тем, что импульсный сигнал излучается постоянно. Длительность импульсов может быть разной. Если она будет очень короткой, то это также может создать проблему для радар-детектора, но большинство современных моделей радар-детекторов оснащены защитой от импульсного радара.</p>
