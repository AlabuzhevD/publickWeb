 <p>Датчик автоматически измеряет скорость всех транспортных средств в зоне контроля и сохраняет две фотографии для каждого нарушителя: общим планом (групповой снимок всей зоны контроля с выделением данного нарушителя) и крупным планом (фотография нарушителя с визуально различимым номерным знаком).</p>
 <p>Данные о нарушении включают в себя также распознанный номер, зафиксированную скорость ТС, метку о движении по полосе общественного транспорта, направление движения, дату и время нарушения, значение максимально допустимой скорости на данном участке дороги, название контролируемого участка, географические координаты, серийный номер комплекса, номер и срок действия свидетельства об утверждении типа средств измерений.</p>
