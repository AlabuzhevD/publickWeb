<h2>Описание работы радара</h2>
                <p>Фоторадарный датчик устанавливается сбоку от проезжей части на опорах освещения или над проезжей частью дороги. </p>
                <p>Какие нарушения правил дорожного движения фиксирует фоторадар Кордон:</p>
                <ul>
                    <li>Автоматическое измерение скоростей, определение положения и фотофиксация всех ТС на четырех полосах дорожного движения в обоих направлениях.</li>
                    <li>Автоматическое фиксирование других нарушений ПДД: движение по обочине и полосе для общественного транспорта, выезд на полосу встречного движения.</li>
                    <li>Возможность установки порогов скорости для каждой отдельной полосы и различных категорий ТС (грузовые, легковые).</li>
                    <li>Автоматическое распознавание различных форматов номерных знаков стран СНГ и Европы.</li>
                    <li>Возможность проверки распознанных номеров по различным федеральным и региональным базам розыска.</li>
                    <li>Передача данных о зафиксированных нарушителях в единую централизованную базу данных по проводным или беспроводным каналам связи.</li>
                    <li>Инфракрасная подсветка для обеспечения работы в ночное время.</li>
                    <li>Сбор статистических данных об интенсивности транспортного потока.</li>
                </ul>
